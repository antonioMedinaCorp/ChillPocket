    <h4>Sinopsis</h4>
    <p>Kingdom Come plantea una humanidad repleta de metahumanos (seres con superpoderes), los cuales utilizan sus habilidades para su beneficio o para aplicar una forma distorsionada de justicia. Esto los enfrenta con los miembros de la Liga de la Justicia, contando la nueva generación y el apoyo de los héroes originales: Superman, la Mujer Maravilla, Batman, Linterna Verde y Flash, entre otros, que regresan después que una explosión nuclear destruyera Kansas (el hogar de Superman) este hecho los devuelve a la palestra en medio de una nueva sociedad de villanos liderada por Lex Luthor y las Organización de las Naciones Unidas en contra de los metahumanos. </p>
