      <h4>Crítica</h4>
    <p>Kingdom Come es uno de los cómics más influyentes de todos los tiempos, una carta de amor a los superhéroes escrita por Mark Waid e ilustrada como los ángeles por un portento del pincel como Alex Ross. Si todavía no has leído este cómic, no sabemos a qué estás esperando... </p>
    <p>Si tuviéramos que intentar explicar qué son los superhéroes para nosotros, cómo nos han podido influir de esta manera, por qué los consideramos dioses modernos como los antiguos griegos consideraban así a los Olímpicos... Entonces diríamos que deberían leer Kingdom Come. Probablemente, en toda la historia del noveno arte, no se ha escrito jamás una carta de amor tan profunda, melancólica y esperanzadora como la que Mark Waid escribió y Alex Ross dibujó en 1996. Y decimos "dibujó" cuando, probablemente, deberíamos estar hablando de obra de arte en mayúsculas por parte del ilustrador, quien tanto a nivel de diseños de personajes como a nivel de escenarios alcanza la absoluta perfección y nos regala la Capilla Sixtina del mundo del cómic (entiéndase la referencia).</p>
    <p>Y eso que la historia de Kingdom Come (no confundir con el videojuego) no tiene nada de especial, sobre todo si la valoramos ahora, con todas las historias de superhéroes que ha habido después de ella. El cómic de Kingdom Come parte de la premisa de que una nueva generación de héroes ha dejado atrás a los componentes de la Liga de la Justicia, la Sociedad de la Justicia de América y compañía. Todos los grandes superhéroes del Universo DC, a excepción de un omnipresente Batman que se niega a dejar Gotham City al amparo de nadie que no sea él, han tirado la toalla. Y todo, a raíz del adiós de Superman y la decisión de abandonar la capa roja en favor de las nuevas generaciones. Un relevo generacional duro que no es, ni mucho menos, el tema central del cómic.</p>
    <p>Así pues, el cómic Kingdom Come parte de la base de un cansancio generacional de los héroes del pasado y un abandono de la profesión. Uno podría decirse que, como antesala, la premisa es sensacional, especialmente por todas las lecturas humanas que podemos desprender acerca del paso del tiempo y la aceptación del mismo. Sin embargo, pronto Mark Waid nos descubre que no, que el cómic no va de eso, que los superhéroes del pasado siempre han sido los superhéroes del futuro y que las generaciones no están para ser enterradas en la arena, sino para abrazarse mutuamente y progresar. Que solamente mirando hacia el futuro cuando coges la mano del pasado es cómo descubriremos la verdad y el camino para llegar a ella.</p>
    <p>Es curioso cómo Kingdom Come vio la luz a mediados de los 90, la década en la que el cómic de superhéroes vivía una profunda crisis existencial, merced a años y años de publicaciones, a la sequía de ideas que pusieran fin a las repeticiones constantes de argumentos, tramas y sorpresas y a la vorágine de dibujantes que estaban más interesados de mostrarnos sus conocimientos anatómicos del cuerpo humano que de servir al guion con ilustraciones adecuadas, coherentes, lógicas y poderosas. Por eso resulta tan paradójico que la mayor carta de amor que se ha escrito jamás en la historia del cómic al mundo de los superhéroes se enmarque dentro de la producción comiquera de una década consagrada al olvido por parte de la mayoría de los lectores.</p>
    <p>Pero es que Kingdom Come es una proeza literaria sin límites. Hablemos en términos de literatura. Mark Waid tenía claro que no quería hacer "un cómic más", una nueva producción de DC Comics con superhéroes, villanos, peleas masivas, diálogos mordaces y un largo etcétera de tropos usados hasta la extenuación en el noventa por ciento de las obras de la industrias. Para ello, Waid ideó una historia a gran escala y sometida al sello Elseworlds (Otros Mundos), para no tener que rendir cuentas ante el sagrado canon y la maldita continuidad. Partiendo de un cuadro en blanco sobre el que podía desarrollar con profundidad todos los temas que quería contar, se unió a un Alex Ross entregado, que ejerció un milagroso trabajo como ilustrador y llevó el gran guion de Waid a su máxima expresión. No se concibe Kingdom Come sin Waid y Ross, a pesar de que la fama que ha ganado el dibujante entre el público de masas gracias a este título sea completamente merecida.</p>
